  <h2 id="Safety">Is It Safe to Fly During the Pandemic?</h2>
  
  <p>Wide-spread vaccination holds promise for ending the coronavirus disease 2019 (COVID-19) pandemic, but it
     won't happen overnight.In the meantime, continue taking precautions to protect yourself and others, 
     especially if you must travel.</p>
     
     <b style="margin-left: 10px;">Before you travel</b>
     <ul>
      <p>As you think about making travel plans, consider these questions</p>
      <div>
        <b style="color: black;">1. Is COVID-19 spreading where you live or at your destination?</b>
        <br>The more cases in your community or at your destination, the more
        likely you are to get and spread COVID-19 during travel. 
      </div><br>

      <div>
        <b style="color: black;">2. Are you at increased risk for severe illness?</b>
        <br>Anyone can get COVID-19, but older adults and people of any age with certain medical conditions are at
        increased risk for severe illness from COVID-19.</div><br>
        
      <div>
        <b style="color: black;">3.Do you live with someone who's at increased risk for severe illness?</b>
        <br>If you get infected while traveling, you can spread the virus to the people you live with when
        you return, even if you don't have symptoms.</div><br>
      </ul>
      
